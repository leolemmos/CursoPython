# Operadores Aritiméticos
valor1 = 10
valor2 = 2

# Adição +
total= valor1 + valor2

print(total)

# Subtração -
subtracao = valor1 - valor2

print(subtracao)

# Multiplicação *
produto = valor1 * valor2

print(produto)

# Divisão /
quociente = valor1 / valor2

print(quociente)

# Potência
potencia = valor1 ** valor2

print(potencia)
